    private void btn_searchFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchFileActionPerformed
        readers.setParams();
        memory.setMemory();
        memory.clean();
        memory.createMemory();
        // se trata el archivo para obtener las instrucciones
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        FileNameExtensionFilter fileFilter = new FileNameExtensionFilter("Files asm", "asm"); 
        fileChooser.setFileFilter(fileFilter);
        int result = fileChooser.showOpenDialog(this);
        File selectFile = fileChooser.getSelectedFile();
        fileLines = readers.readFile(selectFile);
        
        if (result != JFileChooser.CANCEL_OPTION) {
            File fileName = fileChooser.getSelectedFile();
            if ((fileName == null) || (fileName.getName().equals(""))) {
                txt_file.setText("...");
            } else {
                txt_file.setText(fileName.getName());
            }
        }
        
        for (int i = 0; i < fileLines.size(); i++) {
            linesBin.add(convert.changeFormat(fileLines.get(i)));
        }
        Memory memoryLine = new Memory(fileLines, linesBin);
        
        //se procesa linea por linea
        for (int i = 0; i < fileLines.size(); i++) {
            memoryLine.executeLine(i);
            ArrayList<Object> listInstruction = new ArrayList<>();
            listInstruction.add(memoryLine.getCodAsm().get(i));
            listInstruction.add(memoryLine.getCodBin().get(i));
            listInstruction.add(convert.getIR(fileLines.get(i)));
            listInstruction.add(memoryLine.asigmentMemory(fileLines.get(i)));
            listInstruction.add(memoryLine.getAC());
            listInstruction.add(memoryLine.getAX());
            listInstruction.add(memoryLine.getBX());
            listInstruction.add(memoryLine.getCX());
            listInstruction.add(memoryLine.getDX());
            listPrimary.add(listInstruction);
        }
    }//GEN-LAST:event_btn_searchFileActionPerformed

